char gobblespaces()
{
char c;
for ( ; (c = input()) != 0 && isspace(c); )
if (c == '\n')
{
thisfile_line++;
thisfile_column = 0;
}
else
thisfile_column++;
if (c)
thisfile_column++;
return (c);
}
void gobblecomment()
{
char c, done = 0;
for ( ; !done && (c = input()) != 0; )
{
if (c == '\n')
{
thisfile_line++;               
thisfile_column = 0;
continue;
}
thisfile_column++;
if (c == '*')
{
while ((c = input()) == '*')
thisfile_column++;
if (c == '\n')
{
thisfile_line++;             
thisfile_column = 0;
}
else
{
thisfile_column++;
if (c == '/')
done = 1;
}
}
}
}
int sc_scan_attribute(char **string)
{
char c, done = 0, text[SCANNER_STR_SIZES];
int  n;
c = gobblespaces();
if (c != '(' || ((c = input()) != '(')) return (-1);
thisfile_column++;
text[0] = text[1] = '(';
for (n = 2; !done && (c = input()) != 0 && n < SCANNER_STR_SIZES; )
{
text[n++] = c;
if (c == '\n')
{
thisfile_line++;               
thisfile_column = 0;
continue;
}
thisfile_column++;
if (c == ')')
{
if ((c = input()) == ')')
done = 1;
text[n++] = c;
if (done)
{
text[n++] = 0;
*string = strdup(text);
}
thisfile_column++;
}
}
return ( (c == 0 || n >= SCANNER_STR_SIZES) ? -1 : 0 );
}
